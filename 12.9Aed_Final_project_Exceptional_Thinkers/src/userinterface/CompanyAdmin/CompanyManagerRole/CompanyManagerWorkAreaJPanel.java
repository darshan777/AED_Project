    public void populateJobList()
    {
        DefaultTableModel model = (DefaultTableModel)jobPostingTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request : userAccount.getWorkQueue().getWorkRequestList())
        {
           Object[] row = new Object[4];
           row[0] = request;
           row[1] = request.getJob().getJobType();
           row[2] = request.getJob().getJobLoc();
           row[3] = request.getJob().getJobId();
           model.addRow(row);
        }
    }
    
    public void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel)appliedStudentsTable.getModel();
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList())
        {
            Object[] row = new Object[8];
            row[0] = request;
            row[1] = ((AppliedJobRequest)request).getAppliedJobRequest(); 
            row[2] = request.getRequestDate();
            row[3] = request.getJob().getJobType();
            row[4] = request.getJob().getJobLoc();
            row[5] = request.getReceiver();
            row[6] =((AppliedJobRequest)request).getStream();
            row[7] = enterprise.getName();
            model.addRow(row);
        }
    }

