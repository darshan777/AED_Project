    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        
        int selectedRow = appliedStudentsTable.getSelectedRow();
        if (selectedRow < 0)
        {
            return;
        }
        
        AppliedJobRequest request = (AppliedJobRequest)appliedStudentsTable.getValueAt(selectedRow, 0);
        request.setStatus("Processing");
        ProcessJobApplicationsJPanel processJobApplicationsJPanel = new ProcessJobApplicationsJPanel(userProcessContainer, request);
        userProcessContainer.add("processJobApplicationsJPanel", processJobApplicationsJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_processJButtonActionPerformed

