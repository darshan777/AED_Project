    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        // TODO add your handling code here:

        int selectedjob = cTable.getSelectedRow();
        if(selectedjob >=0)
        {

            WorkRequest jobselected = (WorkRequest)cTable.getValueAt(selectedjob, 0);
            String name = ((JobResultWorkRequest)jobselected).getEnterpriseName();
            String message = "FromCoopToStudent";
            //String date="Today";
            
                Date newDate= new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss"); //Or whatever format fits best your needs.
             String dateStr = sdf.format(newDate);

            JobResultWorkRequest request = new JobResultWorkRequest();
            request.setMessage(message);
            request.setJob(jobselected.getJob());
            request.setSender(account);
            request.setRequestDate(dateStr);
            request.setStatus("Sent");
            request.setEnterpriseName(name);
            
            Organization org1 = null;

            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) 
            {
                if (organization instanceof StudentOrganization) 
                {
                    org1 = organization; 
                    break;
                }
            }
   
            if (org1!=null)
            {
                org1.getWorkQueue().getWorkRequestList().add(request);
                account.getWorkQueue().getWorkRequestList().add(request);
                JOptionPane.showMessageDialog(null, "Successful");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Please select any row ");
            }

        }

    }//GEN-LAST:event_btnApplyActionPerformed

    private void btnassignMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnassignMeActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = cTable.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }
        
         WorkRequest request = (WorkRequest)cTable.getValueAt(selectedRow, 0);
        request.setReceiver(account);
        request.setStatus("Pending");
        populateJob();
        
        JOptionPane.showMessageDialog(null, "Assigned Successfully");
    }//GEN-LAST:event_btnassignMeActionPerformed

