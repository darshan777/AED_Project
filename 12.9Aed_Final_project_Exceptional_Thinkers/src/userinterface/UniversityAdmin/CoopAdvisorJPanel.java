      private void populateJob(){
    
        DefaultTableModel model = (DefaultTableModel)cTable.getModel();
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList())
        {
        Object row[] = new Object[7];
                
                row[0] = request;
                row[1] = request.getJob().getJobId();
                row[2] = request.getJob().getJobType();
                row[3] = request.getJob().getJobLoc();
                row[4] = request.getJob().getJobDesc();
                row[5] = request.getSender().getUsername();
                
                
                //row[6] = request.getReceiver();
                
                model.addRow(row);
        }
    
    }
      
      
public void populateAppliedJob() {
        DefaultTableModel model = (DefaultTableModel) appliedJobTable.getModel();
        model.setRowCount(0);
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof StudentOrganization) {
                for (UserAccount acc : organization.getUserAccountDirectory().getUserAccountList()) {
                    for (WorkRequest wr : acc.getWorkQueue().getWorkRequestList()) {
                        if(wr instanceof AppliedJobRequest)
                        {
                        AppliedJobRequest ajr = (AppliedJobRequest) wr;
                        if (null != ajr.getTestResult() &&
                                ajr.getTestResult().equals(("Reject"))) 
                        {
                            Object[] row = new Object[7];
                            row[0] = ajr;
                            row[1] = ajr.getRequestDate();
                            row[2] = ajr.getStatus();
                            row[3] = ajr.getJob().getJobLoc();
                            row[4] = ajr.getJob().getJobType();
                            row[5] = ajr.getEntName();
                            row[6] = ajr.getTestResult();

                            model.addRow(row);

                        }
                        }
                    }
                }

            }
        }

    }
      

